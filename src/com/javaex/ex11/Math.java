package com.javaex.ex11;

public class Math {
	
	public static int plus(int a, int b) {
		int sum = a+b;
		return sum;
	}
	public static double plus(double a, double b) {
		double sum = a-b;
		return sum;
	}
	
	public static double circleArea(int radius) {
		double area =  3.14*radius*radius;
		return area;
	}
}
